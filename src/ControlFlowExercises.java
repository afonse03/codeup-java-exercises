// control statements and loops exercise

public class ControlFlowExercises {
    public static void main(String[] args) {
        int i = 5; // integer variable with a value of
        while (i <= 15) { // this is a while loop that runs so long as i is less than or equal to 15
            System.out.println(i);
            i++; // each loop iteration, output the current value of i, then increment i by one
        }
    }
}
